package animals;

public class Animal {
    public Animal(){

    }
    public int age;
    public String species;
    public boolean isAlive;

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getSpecies() {
        return species;
    }

    public void setSpecies(String species) {
        this.species = species;
    }

    public void setAlive(boolean alive) {
        isAlive = alive;
    }





}
